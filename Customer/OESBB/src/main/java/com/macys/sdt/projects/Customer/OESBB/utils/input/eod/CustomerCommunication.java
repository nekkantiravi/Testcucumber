package com.macys.sdt.projects.Customer.OESBB.utils.input.eod;


public class CustomerCommunication {
	private String mailSourceId;
	private String mailTypeCode;
	private String mailSubtypeCode;
	private String partyType;

	public String getMailSourceId() {
		return mailSourceId;
	}

	public void setMailSourceId(String mailSourceId) {
		this.mailSourceId = mailSourceId;
	}

	public String getMailTypeCode() {
		return mailTypeCode;
	}

	public void setMailTypeCode(String mailTypeCode) {
		this.mailTypeCode = mailTypeCode;
	}

	public String getMailSubtypeCode() {
		return mailSubtypeCode;
	}

	public void setMailSubtypeCode(String mailSubtypeCode) {
		this.mailSubtypeCode = mailSubtypeCode;
	}

	public String getPartyType() {
		return partyType;
	}

	public void setPartyType(String partyType) {
		this.partyType = partyType;
	}

}
